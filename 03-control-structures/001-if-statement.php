<?php
    // if () statement is a logical expression in PHP which helps us execute something based on certain conditions
    // else if () can be used along with if () to do something else based on certain other condition
    if (3 > 10) {
        echo "Three is greater than ten";
    }
    else if (3 < 10) {
        echo "Three is less than ten";
    }
    else
        echo "Three is equal to ten";
?>